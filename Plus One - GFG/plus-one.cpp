class Solution {
  public:
    vector<int> increment(vector<int> digits ,int N) {
        // code here
        bool carry = true;
        
        for(int i=N-1; i >= 0 && carry; i--) {
            carry = (++digits[i]%=10) == 0;
        }

        if(carry) {
            digits.insert(digits.begin(), 1);
        }
    
        return digits;
    }
};

